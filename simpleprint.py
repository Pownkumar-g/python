#In python we use simple print Keyword to print any statement under double quotation 
print("Hello World !")
print("My","name","is","Amit")
#The sep keyword is used to separate the print statement at the comma place
print("My","name","is","Amit",sep='***')
#The end keyword is used to print the end value in the print statement at the end comma
print("What is you name " ,end='?')
print("My name is Amit")